<h2>Frågor</h2>
<p>Grundtanken med <em>spelaihop</em> är att ställa och besvara frågor på något sätt
handlar om video- eller datorspel.</p>
<p>Det finns inga dumma frågor - men däremot
går det att ställa frågor på mindre bra sätt. Försök vara så tydlig du kan
när du skriver din fråga - om våra andra användare inte förstår din fråga så
kanske de röstar ned din fråga, och då är chansen ännu mindre att du får svar.</p>

<h2>Svar</h2>

<p>Samma sak gäller för svar. Tydlighet varar längst. Om du ger ett bra och tydligt
svar så kan du få positiva röster på ditt inlägg. Om frågeställaren anser att
ditt svar är det bästa, så kan hen dessutom markera ditt svar som korrekt.</p>


<h2>Rykte</h2>

<p>Ditt rykte baserar sig på dina aktiviteter. Du får poäng tilldelad dig när
du ställer och besvarar frågor. Du får minuspoäng om dina inlägg
blir nedröstade och extra poäng per upp-röst.</p>

<p>Ryktet kan spela roll när andra personer sitter och funderar på om det
du skrivit verkligen är sant eller inte. Om du har ett bra rykte så är det
större sannolikhet att du är någon som går att lita på.</p>

